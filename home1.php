      <h5>Q: Do I need to create an account to book bus tickets on BusBus?</h5>
  
        <p>A: No, you can book bus tickets as a guest user by providing required passenger details. However, we recommend you to create an account so that you get latest information about bus availability, ticket details and other features which will help you book faster during future transactions</p>
        <h5>Q: Can I cancel my bus ticket at any time?</h5>
  
         <p> A: Yes, you can. Once the ticket booking is done, all you have to do is login in to the AbhiBus website or mobile app and follow the cancellation process.</p>
      <h5>Q: Can I book an RTC bus ticket on AbhiBus?</h5>
       <p> A: Yes, you can. AbhiBus has extended its bus booking services to a number of RTCs in India. Some of these RTCs are Andhra Pradesh State Road Transport Corporation (APSRTC), Assam State Transport Corporation (ASTC), Bihar State Tourism Development Corporation (BSTDC), Gujarat State Road Transport Corporation (GSRTC), Himachal Road Transport Corporation (HRTC), Jammu and Kashmir State Road Transport Corporation (JKSRTC), Kerala RTC, Kadamba Transport Corporation (KTCL), Maharashtra State Road Transport Corporation (MSRTC), Odisha State Road Transport Corporation (OSRTC), Patiala and East Punjab States Union (PEPSU), Puducherry Road Transport Corporation (PRTC), Rajasthan State Road Transport Corporation (RSRTC), South Bengal State Transport Corporation (SBSTC), Tamil Nadu State Transport Corporation (TNSTC), Uttar Pradesh State Road Transport Corporation (UPSRTC), Uttarakhand Transport Corporation (UTC), West Bengal Transport Corporation [WBTC (CTC).</p>
      
  
        <h5>Q: Can I get Discounts & Offers on bus tickets?</h5>
  
          <p>A: Yes, you will get the lowest bus ticket fares on AbhiBus by applying coupon codes before completing your transaction. To get the best price on bus bookings check AbhiBus offers page.</p>
          <h5>Q: What are the payment options that AbhiBus accepts?</h5>
  
          <p>A: We accept various payment options to complete your booking like Debit card, Credit card, Net banking, Wallets, UPI, cash cards and more.</p>
          <h5>Q: Is it required to carry a print out of my bus ticket to board a bus?</h5>
  
            <p>A: No, it's not compulsory. You can show your M-ticket or e-ticket on your mobile device before getting into the bus. But It is advisable to carry a government issued Identity card to verify your identity before boarding the bus.</p>
          <h5>
         
          Q: Is it safe to travel by bus during the COVID-19 pandemic?</h5>
         <p> A: AbhiBus, along with bus operators under the AbhiBus banner, have been doing a lot when it comes to upholding the safety standards that have been set by the Government of India and the MoHFW. Passengers can make their bus booking through the AbhiBus platform and travel safely with AbhiBus.</p>
        <h5>Q: What is Safety+, and what does it have to do with AbhiBus?</h5>
          <p>A: Safety+ is a new feature that customers can look for when making a bus ticket booking on the AbhiBus platform. Bus operators follow strict safety procedures such as sanitizing buses after every trip, staff with masks, temperate check of both passengers and bus staff before boarding the bus, and much more. Look for the Safety+ icon, which is denoted by a shield with a “plus” sign in the middle when you book your bus tickets online with AbhiBus.</p>
       <h5>Q: Is it safer to travel by bus or by plane during COVID-19?</h5>
        <p>A: Though a flight might save some travel time as it does go from Point A to Point B a lot faster than a bus, it is advisable to travel by bus instead. If a passenger wishes to travel via airplane, they would have to get to the terminal early and wait for a long time before boarding a flight. There is a risk of infection as the air in the plane is circulated within the sealed tube and could pose a danger if an asymptomatic passenger does carry the virus onboard. With a bus, the passenger can wait in their vehicle before boarding the bus, and therefore, crowding at a boarding point is significantly reduced. Passengers can even keep their windows open to ensure that fresh air is circulated in the bus. Measures followed on Safety+ buses considerably minimize the chance of contracting the virus on a bus than an airplane.</p>
         
